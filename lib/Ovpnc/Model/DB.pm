package Ovpnc::Model::DB;
use File::Slurp;
use strict;
use base 'Catalyst::Model::DBIC::Schema';

__PACKAGE__->config(
    schema_class => 'Ovpnc::Schema',

    connect_info => {
        dsn => 'dbi:mysql:ovpnc',
        user => 'ovpnc',
        password => &get_pass,
        AutoCommit => q{1},
    }
);

sub get_pass{
    return read_file('config/.mysql', chomp => 1)
        or die "Cannot read SQL passwd file: " . $!;
}

